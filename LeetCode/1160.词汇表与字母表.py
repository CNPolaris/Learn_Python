class Solution(object):
    def countCharacters(self, words, chars):
        """
        :type words: List[str]
        :type chars: str
        :rtype: int
        """
        res=0
        for ch in words:
            t=False
            for i in ch:
                if ch.count(i) <=chars.count(i):
                    t=True
                    continue
                else:
                    t=False
                    break
            if t==True:
                res+=len(ch)
        return res
